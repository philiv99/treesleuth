// Export a map for quick lookups by ID
export const speciesById: Map<string, TreeSpecies> = new Map(
  treeSpecies.map(species => [species.id, species])
)

// Get species by ID
export function getSpeciesById(id: string): TreeSpecies | undefined {
  return speciesById.get(id)
}

// Get all species for a family
export function getSpeciesByFamily(family: TreeSpecies['family']): TreeSpecies[] {
  return treeSpecies.filter(s => s.family === family)
}

// Get all species for a region
export function getSpeciesByRegion(region: TreeSpecies['regions'][number]): TreeSpecies[] {
  return treeSpecies.filter(s => s.regions.includes(region))
}

// Get a random species
export function getRandomSpecies(): TreeSpecies {
  return treeSpecies[Math.floor(Math.random() * treeSpecies.length)]
}

// Get species excluding certain IDs
export function getSpeciesExcluding(excludeIds: string[]): TreeSpecies[] {
  return treeSpecies.filter(s => !excludeIds.includes(s.id))
}
